#Найти расстояние между двумя точками пространства
#AB = √(xb - xa)2 + (yb - ya)2 + (zb - za)2

point_a_x = int(input("Введите координату х для точки А: "))
point_a_y = int(input("Введите координату y для точки А: "))
point_a_z = int(input("Введите координату z для точки А: "))

point_b_x = int(input("Введите координату х для точки B: "))
point_b_y = int(input("Введите координату y для точки B: "))
point_b_z = int(input("Введите координату z для точки B: "))

ab = ((point_b_x - point_a_x) ** 2 + (point_b_y - point_a_y) ** 2 + (point_b_z - point_a_z) ** 2) ** 0.5

print('Расстояние между двумя точками пространства равна', ab)